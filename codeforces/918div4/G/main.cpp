int main(int argc, char *argv[])
{
  { int _; cin >> _; }
  int n, m;
  while (cin >> n >> m) {
    const int N = 1000 + 3;
    const int S = 1000 + 3;
    vec<pair<int, lli>> g[N];
    for (int i = 0; i < m; ++i) {
      int a, b, c;
      cin >> a >> b >> c;
      --a;
      --b;
      g[a].push_back(make_pair(b, c));
      g[b].push_back(make_pair(a, c));
    }
    vec<int> s(n);
    cin >> s;
    const int mxs = *max_element(s.begin(), s.end());

    priority_queue<tuple<lli, int, int>> q;
    static lli cost[N][N];
    const lli inf = 1LL << 60;
    for (int i = 0; i < n; ++i) {
      for (int j = 0; j <= mxs; ++j) {
        cost[i][j] = inf;
      }
    }
    q.push(make_tuple(0LL, 0, s[0]));
    cost[0][s[0]] = 0;
    while (q.size()) {
      auto [c, curr, slowness] = q.top();
      c = abs(c);
      q.pop();
      if (cost[curr][slowness] != c) continue;
      each (e, g[curr]) {
        auto [next, w] = e;
        int ns = min(slowness, s[next]);
        if (cost[next][ns] > c + slowness * w) {
          cost[next][ns] = c + slowness * w;
          q.push(make_tuple(-cost[next][ns], next, ns));
        }
      }
    }
    lli z = inf;
    for (int i = 0; i <= mxs; ++i) {
      setmin(z, cost[n - 1][i]);
    }
    cout << z << endl;
  }
  return 0;
}

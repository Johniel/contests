namespace math {
  lli extgcd(lli a, lli b, lli& x, lli& y)
  {
    lli g = a;
    x = 1;
    y = 0;
    if (b != 0) {
      g = extgcd(b, a % b, y, x);
      y -= (a / b) * x;
    }
    return g;
  }

  lli mod_inverse(lli a, lli m)
  {
    lli x, y;
    extgcd(a, m, x, y);
    return (m + x % m) % m;
  }

  lli mod_pow(lli n, lli p, lli mod)
  {
    if (p == 0) return 1;
    if (p == 1) return n % mod;
    lli m = mod_pow(n, p / 2, mod);
    (m *= m) %= mod;
    if (p % 2) m = (m * n) % mod;
    return m;
  }
};


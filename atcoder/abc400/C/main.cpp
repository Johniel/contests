lli isqrt2(lli x) {
    lli l = 0, r = min(x, 1'000'000'001LL);
    while (l < r) {
        lli m = (l + r + 1) / 2;
        if (m <= x / m) l = m;
        else r = m - 1;
    }
    return l;
}


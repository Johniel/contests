constexpr lli mod = 998244353;

namespace math {
  lli extgcd(lli a, lli b, lli& x, lli& y)
  {
    lli g = a;
    x = 1;
    y = 0;
    if (b != 0) {
      g = extgcd(b, a % b, y, x);
      y -= (a / b) * x;
    }
    return g;
  }

  lli mod_inverse(lli a, lli m)
  {
    lli x, y;
    extgcd(a, m, x, y);
    return (m + x % m) % m;
  }

  lli mod_pow(lli n, lli p, lli M = mod)
  {
    if (p == 0) return 1;
    if (p == 1) return n % M;
    lli m = mod_pow(n, p / 2, M);
    m *= m;
    m %= M;
    if (p % 2) m = (m * n) % M;
    return m;
  }
};

struct UnionFind {
  vector<int> r, p;
  UnionFind(int n) {
    r.resize(n, 0);
    p.resize(n, -1);
  }
  void unite(int a, int b) {
    a = find(a);
    b = find(b);
    if (a == b) return ;
    if (r[a] > r[b]) {
      p[a] += p[b];
      p[b] = a;
    } else {
      p[b] += p[a];
      p[a] = b;
      if (r[a] == r[b]) ++r[b];
    }
    return ;
  }
  int find(int a) {
    return (p[a] < 0) ? a : p[a] = find(p[a]);
  }
  bool same(int a, int b) {
    return find(a) == find(b);
  }
  size_t size(int n) {
    return -p.at(find(n));
  }
};

const int N = 4 * 1e5 + 3;
int cnt;
int parent[N];
int S[N];
lli memo[N];
int label[N];
lli rec(int curr)
{
  if (memo[curr] != -1) return memo[curr];
  lli x = rec(parent[curr]);
  (x += S[curr] * math::mod_inverse(S[parent[curr]], mod) % mod) %= mod;
  return memo[curr] = x;
}

int main(int argc, char *argv[])
{
  int n;
  while (cin >> n) {
    fill(S, S + N, 1);
    iota(label, label + n, 0);
    cnt = n;
    UnionFind uf(n*2);
    for (int i = 0; i < n - 1; ++i) {
      int a, b;
      cin >> a >> b;
      --a;
      --b;
      a = uf.find(a);
      b = uf.find(b);
      uf.unite(a, b);
      uf.find(a);
      uf.find(b);
      parent[label[a]] = cnt;
      parent[label[b]] = cnt;
      label[uf.find(a)] = cnt;
      label[uf.find(b)] = cnt;
      S[cnt] = uf.size(a);
      ++cnt;
    }
    fill(memo, memo + N, -1);
    memo[cnt-1] = 0;
    parent[cnt-1] = -2;
    for (int i = 0; i < n; ++i) cout << rec(i) << ' '; cout << endl;
  }
  return 0;
}

    vec<int> idx(e);
    iota(idx.begin(), idx.end(), 0);
    sort(idx.begin(), idx.end(), [&] (int a, int b) { return ord[a] < ord[b]; });
    reverse(idx.begin(), idx.end());

    UnionFind uf(n+m);
    for (int i = n; i < n+m; ++i) {
      uf.unite(n, i);
    }
    vec<int> u;

int main(int argc, char *argv[])
{
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.setf(ios_base::fixed);
  cout.precision(15);

  map<str, int> m;
  {
    str s = "atcoder";
    m[s] = 0;
    queue<str> q;
    for (q.push(s); q.size(); q.pop()) {
      str s = q.front();
      for (int i = 0; i + 1 < s.size(); ++i) {
        str t = s;
        swap(t[i], t[i + 1]);
        if (m.count(t) == 0) {
          m[t] = m[s] + 1;
          q.push(t);
        }
      }
    }
  }

  str s;
  while (cin >> s) {
    cout << m[s] << endl;
  }

  return 0;
}

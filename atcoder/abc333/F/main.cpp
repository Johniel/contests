using lli = long long int;
using ull = unsigned long long;
using str = string;
template<typename T> using vec = vector<T>;

constexpr lli mod = 998244353;

namespace math {
  lli extgcd(lli a, lli b, lli& x, lli& y)
  {
    lli g = a;
    x = 1;
    y = 0;
    if (b != 0) {
      g = extgcd(b, a % b, y, x);
      y -= (a / b) * x;
    }
    return g;
  }

  lli mod_inverse(lli a, lli m)
  {
    lli x, y;
    extgcd(a, m, x, y);
    return (m + x % m) % m;
  }

  lli mod_pow(lli n, lli p, lli M = mod)
  {
    if (p == 0) return 1;
    if (p == 1) return n % M;
    lli m = mod_pow(n, p / 2, M);
    m *= m;
    m %= M;
    if (p % 2) m = (m * n) % M;
    return m;
  }
};

const int N = 3000 + 3;

int main(int argc, char *argv[])
{
  vec<lli> p2;
  p2.push_back(1);
  for (int i = 0; i < N; ++i) {
    p2.push_back(p2.back() * 2 % mod);
  }

  static lli dp[N][N];
  fill(&dp[0][0], &dp[N - 1][N - 1] + 1, 0);
  dp[1][0] = 1;
  for (int remained = 2; remained < N; ++remained) {
    lli x = 0;
    vec<lli> v;
    lli w = math::mod_inverse(4, mod);
    for (int position = remained - 2; 0 <= position; --position) {
      (x += dp[remained - 1][position] * w % mod) %= mod;
      (w *= math::mod_inverse(2, mod)) %= mod;
    }

    const lli y = p2[remained] * math::mod_inverse((p2[remained] - 1 + mod) % mod, mod) % mod;

    dp[remained][0] = x * y % mod;
    for (int position = 1; position < remained; ++position) {
      (x *= math::mod_inverse(2, mod)) %= mod;

      x -= dp[remained - 1][position - 1] * math::mod_inverse(p2[remained + 1], mod) % mod;
      (x += mod) %= mod;

      x += dp[remained - 1][position - 1] * math::mod_inverse(2, mod) % mod;
      x %= mod;

      dp[remained][position] = x * y % mod;
    }
  }

  int n;
  while (cin >> n) {
    for (int i = 0; i < n; ++i) cout << dp[n][i] << ' ';
    cout << endl;
  }

  return 0;
}

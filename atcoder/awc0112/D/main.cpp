using lli = long long int;
using ull = unsigned long long;
using str = string;
template<typename T> using vec = vector<T>;

constexpr lli mod = 998244353; // 1e9 + 7;

int main(int argc, char *argv[])
{
  int n, k;
  while (cin >> n >> k) {
    vec<pair<lli, int>> v(n);
    cin >> v;
    const int N = 200 + 3;
    const int K = 1e4 + 3;
    static lli dp[N][K][2];
    const lli inf = 1LL << 60;
    fill(&dp[0][0][0], &dp[N - 1][K - 1][2 - 1] + 1, -inf);
    dp[0][0][0] = 0;
    for (int i = 0; i < n; ++i) {
      for (int j = 0; j <= k; ++j) {
        if (j + v[i].second <= k) {
          setmax(dp[i + 1][j + v[i].second][1], dp[i][j][0] + v[i].first);
        }
        setmax(dp[i + 1][j][0], dp[i][j][0]);
        setmax(dp[i + 1][j][0], dp[i][j][1]);
      }
    }
    lli z = 0;
    for (int i = 0; i <= k; ++i) {
      setmax(z, dp[n][i][0]);
      setmax(z, dp[n][i][1]);
    }
    cout << z << endl;
  }
  return 0;
}

constexpr array<int, 8> di({0, 1, -1, 0, 1, -1, 1, -1});
constexpr array<int, 8> dj({1, 0, 0, -1, 1, -1, -1, 1});
constexpr lli mod = 998244353;

const int N = 3000 + 3;
const int K = 3000 + 3;
static int memo[N][K];
int rec(int n, int k)
{
  if (k == 0) return n == 0;
  if (n < k) return 0;
  if (n == k) return 1;
  // cout << n << ' ' << k << endl;
  int& ret = memo[n][k];
  if (ret != -1) return ret;
  return ret = (rec(n - 1, k - 1) + rec(n, k * 2)) % mod;
}

int main(int argc, char *argv[])
{
  ios_base::sync_with_stdio(0);
  cin.tie(0);
  cout.setf(ios_base::fixed);
  cout.precision(15);

  int n, k;
  while (cin >> n >> k) {
    if (n < k) {
      cout << 0 << endl;
      continue;
    }
    fill(&memo[0][0], &memo[N - 1][K - 1] + 1, -1);
    cout << rec(n, k) << endl;
  }

  return 0;
}

std::ostream &operator<<(std::ostream &dest, __int128_t value) {
  std::ostream::sentry s(dest);
  if (s) {
    __uint128_t tmp = value < 0 ? -value : value;
    char buffer[128];
    char *d = std::end(buffer);
    do {
      --d;
      *d = "0123456789"[tmp % 10];
      tmp /= 10;
    } while (tmp != 0);
    if (value < 0) {
      --d;
      *d = '-';
    }
    int len = std::end(buffer) - d;
    if (dest.rdbuf()->sputn(d, len) != len) {
      dest.setstate(std::ios_base::badbit);
    }
  }
  return dest;
}

__int128 parse(string &s) {
  __int128 ret = 0;
  for (int i = 0; i < s.length(); i++)
    if ('0' <= s[i] && s[i] <= '9')
      ret = 10 * ret + s[i] - '0';
  return ret;
}

using lll = __int128;

inline lll fn(lll a, lll b) { return (a * a * a) + (a * a * b) + (a * b * b) + (b * b * b); }

int main(int argc, char *argv[])
{
  ios_base::sync_with_stdio(0);
  cin.tie(0);
  cout.setf(ios_base::fixed);
  cout.precision(15);

  str s;
  while (cin >> s) {
    lll n = parse(s);
    str t = "2000000000000000000";
    lll mn = parse(t);
    str u = "1000000000000000000";
    const lll N = parse(u);
    for (lll a = 0; a * a * a <= N; ++a) {
      lll small = 0;
      lll large = a+1;
      while (small + 1 < large) {
        auto mid = (small + large) / 2;
        if (fn(a, mid) < n) small = mid;
        else large = mid;
      }
      auto p = fn(a, small); if (n <= p) setmin(mn, p);
      auto q = fn(a, large); if (n <= q) setmin(mn, q);
      // if (p == 1000000000000000000LL || q == 1000000000000000000LL) cout << make_pair(a, make_pair(small, large)) << endl;
      // cout << mn << ' ' << make_pair(small, large) << ' ' << make_pair(p, q) << endl;
    }
    cout << mn << endl;
  }

  return 0;
}

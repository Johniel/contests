int main(int argc, char *argv[])
{
  ios_base::sync_with_stdio(0);
  cin.tie(0);
  cout.setf(ios_base::fixed);
  cout.precision(15);

  int n;
  while (cin >> n) {
    const int N = 3000 + 1;
    bitset<3001> small[N];
    bitset<3001> large[N];

    static char g[N][N];
    for (int i = 0; i < n; ++i) {
      for (int j = 0; j < n; ++j) {
        cin >> g[i][j];
      }
    }

    for (int i = 0; i < n; ++i) {
      str a, b;
      for (int j = 0; j < n; ++j) {
        if (i == j) a += '0', b += '0';
        if (i < j) {
          a += g[i][j];
          b += '0';
        }
        if (i > j) {
          a += '0';
          b += g[i][j];
        }
      }
      small[i] = bitset<3001>(b);
      large[i] = bitset<3001>(a);
    }

    lli z = 0;
    for (int i = 0; i < n; ++i) {
      for (int k = i + 2; k < n; ++k) {
        if (g[i][k] == '1') z += (large[i] & small[k]).count();
      }
    }
    cout << z << endl;
  }

  return 0;
}

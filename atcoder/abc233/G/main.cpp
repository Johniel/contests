template<typename T>
struct PrefixSum2D {
  vector<vector<T>> sum;
  PrefixSum2D() {}
  PrefixSum2D(vector<vector<T>> v) {
    const int H = v.size() + 1;
    const int W = v[0].size() + 1;
    sum.resize(H);
    fill(sum.begin(), sum.end(), vector<T>(W, 0));
    for (int i = 0; i < v.size(); ++i) {
      for (int j = 0; j < v[i].size(); ++j) {
        sum[i + 1][j + 1] = v[i][j];
      }
    }
    for (int i = 1; i < H; ++i) {
      for (int j = 0; j < W; ++j) {
        sum[i][j] += sum[i - 1][j];
      }
    }
    for (int i = 0; i < H; ++i) {
      for (int j = 1; j < W; ++j) {
        sum[i][j] += sum[i][j - 1];
      }
    }
  }
  T operator () (int ai, int aj, int bi, int bj) const { return this->query(ai, aj, bi, bj); }
  T query(int ai, int aj, int bi, int bj) const {
    assert(0 <= ai && ai < sum.size());
    assert(0 <= bi && bi < sum.size());
    assert(0 <= aj && aj < sum[0].size());
    assert(0 <= bj && bj < sum[0].size());
    assert(ai <= bi && aj <= bj);
    return sum[bi][bj] + sum[ai][aj] - sum[ai][bj] - sum[bi][aj];
  }
};

const int N = 50 + 1;
char g[N][N];
int memo[N][N][N][N];
PrefixSum2D<int> sum;
int rec(int bi, int bj, int ei, int ej)
{
  int& ret = memo[bi][bj][ei][ej];
  if (ret != -1) return ret;
  if (bi == ei) return ret = 0;
  if (bj == ej) return ret = 0;
  int mn = max(ei - bi, ej - bj);
  for (int i = bi; i < ei; ++i) {
    if (sum(i, bj, i + 1, ej) == 0) {
      int x = rec(bi, bj, i, ej);
      int y = rec(i + 1, bj, ei, ej);
      setmin(mn, x + y);
    }
  }
  for (int j = bj; j < ej; ++j) {
    if (sum(bi, j, ei, j + 1) == 0) {
      int x = rec(bi, bj, ei, j);
      int y = rec(bi, j + 1, ei, ej);
      setmin(mn, x + y);
    }
  }
  return ret = mn;
}


template<typename T> using vec = vector<T>;

constexpr lli mod = 998244353; // 1e9 + 7;

const int N = 2 * 1e6 + 3;

namespace math {
  // gcd(a,b)を返す。xとyにはax+by=±gcd(a,b)を満たす数を設定する。
  lli extgcd(lli a, lli b, lli& x, lli& y)
  {
    lli g = a;
    x = 1;
    y = 0;
    if (b != 0) {
      g = extgcd(b, a % b, y, x);
      y -= (a / b) * x;
    }
    return g;
  }

  lli mod_inverse(lli a, lli m)
  {
    lli x, y;
    extgcd(a, m, x, y);
    return (m + x % m) % m;
  }

  lli mod_pow(lli n, lli p, lli M = mod)
  {
    if (p == 0) return 1;
    if (p == 1) return n % M;
    lli m = mod_pow(n, p / 2, M);
    m *= m;
    m %= M;
    if (p % 2) m = (m * n) % M;
    return m;
  }
};

vec<int> g[N];
vec<lli> c;
vec<lli> d;
pair<lli, lli> rec(int curr)
{
  lli rem = c[curr];
  lli x = 1;
  each (next, g[curr]) {
    auto [r, p] = rec(next);
    (x *= p) %= mod;
    rem += r;
  }
  if (rem < d[curr]) throw "";
  for (int i = 0; i < d[curr]; ++i) {
    (x *= rem % mod) %= mod;
    (x *= math::mod_inverse(i + 1, mod)) %= mod;
    --rem;
  }
  return make_pair(rem, x);
}


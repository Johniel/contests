using mint = Fp<mod>;

int main(int argc, char *argv[])
{
  const int P = 37;

  int n, q;
  str s;
  while (cin >> n >> q >> s) {
    vec<mint> p;
    p.push_back(mint(1));
    for (int i = 0; i < s.size(); ++i) {
      p.push_back(p.back() * P);
    }
    str t = s;
    reverse(t.begin(), t.end());
    SegTree<mint> A(s.size(), mint(0), [] (auto a, auto b) { return a + b; });
    SegTree<mint> B(s.size(), mint(0), [] (auto a, auto b) { return a + b; });
    for (int i = 0; i < s.size(); ++i) {
      A.set(i, mint(s[i] - 'a' + 1) * p[i]);
      B.set(i, mint(t[i] - 'a' + 1) * p[i]);
    }

    while (q--) {
      int op;
      cin >> op;
      if (op == 1) {
        int i;
        char c;
        cin >> i >> c;
        --i;
        int j = n - i - 1;
        s[i] = t[j] = c;
        A.set(i, mint(s[i] - 'a' + 1) * p[i]);
        B.set(j, mint(t[j] - 'a' + 1) * p[j]);
      }
      if (op == 2) {
        int begin, end;
        cin >> begin >> end;
        --begin;
        --end;
        int rbegin = n - end - 1;
        int rend = n - begin - 1;
        auto x = A(begin, end + 1) / p[begin];
        auto y = B(rbegin, rend + 1) / p[rbegin];
        cout << (x == y ? "Yes" : "No") << endl;
      }
    }
  }

  return 0;
}

int main(int argc, char *argv[])
{
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.setf(ios_base::fixed);
  cout.precision(15);

  int n;
  str s, t;
  while (cin >> n >> s >> t) {
    str u = t + t + t + "|" + s + s + s + "$";
    vec<int> v = suffix_array(u);
    lli z = 0;
    lli x = 0;

    auto is_t = [&] (int i) {
      return (s.size() <= i && i < s.size() * 2);
    };
    auto is_s = [&] (int i) {
      int j = s.size() * 3 + 1 + t.size();
      return (j <= i && i < j + t.size());
    };

    each (i, v) {
      if (!is_s(i) && !is_t(i)) continue;
      if (is_s(i)) {
        ++x;
      } else {
        z += x;
      }
    }
    cout << z << endl;
  }

  return 0;
}

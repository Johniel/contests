template<typename T> using vec = vector<T>;

constexpr lli mod = 998244353; // 1e9 + 7;

template<typename T = long long int>
struct BIT {
  vector<T> v;
  const int n;

  BIT(int n_ = 0) : n(n_), v(n_ + 1, 0) {}

  T operator () (int i) const {
    T s = 0;
    while (i > 0) {
      (s += v.at(i)) %= mod;
      i -= i & -i;
    }
    return s;
  }

  T query(int begin, int end) const {
    assert(begin <= end);
    return (*this)(end) - (*this)(begin);
  }

  T operator () (int begin, int end) const {
    return query(begin, end);
  }

  void add(int i, T x) {
    ++i;
    while (i <= n) {
      v.at(i) += x;
      i += i & -i;
    }
    return ;
  }

  size_t size(void) const { return n; }
};

int main(int argc, char *argv[])
{
  int n, m;
  while (cin >> n >> m) {
    vec<lli> a(n), b(m);
    cin >> a >> b;
    sort(a.begin(), a.end());
    sort(b.begin(), b.end());
    BIT<lli> bit(a.size());
    for (int i = 0; i < a.size(); ++i) {
      bit.add(i, a[i]);
    }

    // cout << a << endl;
    // cout << b << endl;

    lli z = 0;
    for (int i = 0; i < b.size(); ++i) {
      auto itr = lower_bound(a.begin(), a.end(), b[i] + 1);
      const int idx = itr - a.begin();

      // cout << b[i] << ' ' << idx << "," << -bit(0, idx) << ", +" << idx * b[i] << endl;
      // cout << (a.size() - idx) <<"*" <<b[i] << endl;
      z += (idx * b[i] % mod) - bit(0, idx) + mod;
      z %= mod;
      z += bit(idx, bit.size()) - ((a.size() - idx) * b[i] % mod) + mod;
      z %= mod;
    }

    cout << z << endl;
  }
  return 0;
}

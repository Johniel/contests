{
  int n, m;
  while (cin >> n >> m) {
    vec<vec<lli>> g(n, vec<lli>(n));
    cin >> g;

    lli z = 0;
    for (int _ = 0; _ < 4; ++_) {
      PrefixSum2D<lli> sum(g);
      SegmentTree2D seg(n, n, [] (lli a, lli b) {return max(a, b); }, -(1LL << 60));
      for (int i = 0; i + m <= n; ++i) {
        for (int j = 0; j + m <= n; ++j) {
          lli x = sum(i, j, i + m, j + m);
          seg.update(i, j, x);
        }
      }
      for (int i = 0; i + m <= n; ++i) {
        for (int j = 0; j + m <= n; ++j) {
          lli a = seg.query(0, 0, i - m + 1, j - m + 1);
          lli b = seg.query(0, j, i - m + 1, n - m + 1);
          lli c = seg.query(i, 0, n - m + 1, n - m + 1);
          setmax(z, a + b + c);
        }
        {
          lli a = seg.query(0, 0, i - m + 1, n - m + 1);
          lli b = seg.query(i, 0, n - m + 1, n - m + 1);
          setmax(z, a + b);
        }
        {
          int j = i + m;
          lli a = seg.query(0, 0, i - m + 1, n - m + 1);
          lli b = seg.query(i, 0, j - m + 1, n - m + 1);
          lli c = seg.query(j, 0, n - m + 1, n - m + 1);
          setmax(z, a + b + c);
        }
      }
      vec<vec<lli>> tmp = g;
      for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
          g[i][j] = tmp[n - j - 1][i];
        }
      }
    }
    cout << z << endl;
  }
  return 0;
}
